namespace Methods.Enums
{
    /// <summary>
    /// Represents the type of a constraint in a LPP.
    /// </summary>
    public enum ConstraintType
    {
        /// <summary>
        /// Less than or equal to constraint.
        /// </summary>
        LessThanOrEqual,
        /// <summary>
        /// Greater than or equal to constraint.
        /// </summary>
        GreaterThanOrEqual,
        /// <summary>
        /// Equality constraint.
        /// </summary>
        Equal
    }
}
